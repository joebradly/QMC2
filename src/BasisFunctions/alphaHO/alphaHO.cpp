double alphaHO_0::eval(const Walker* walker, int i) {
    
    
    //1
    H = 1;
    return H * (*exp_factor);
    
}

double dell_alphaHO_0_x::eval(const Walker* walker, int i) {
    
    
    //-k2*x
    H = -(*k2)*walker->r(i, 0);
    return H * (*exp_factor);
    
}

double dell_alphaHO_0_y::eval(const Walker* walker, int i) {
    
    
    //-k2*y
    H = -(*k2)*walker->r(i, 1);
    return H * (*exp_factor);
    
}

double lapl_alphaHO_0::eval(const Walker* walker, int i) {
    
    
    //k2*(k2*r2 - 2)
    H = (*k2)*((*k2)*walker->get_r_i2(i) - 2);
    return H * (*exp_factor);
    
}

